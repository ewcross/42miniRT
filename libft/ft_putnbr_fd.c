#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long nb;
	char ch;

	nb = n;
	if (nb < 0)
	{
		write(fd, "-", 1);
		nb *= -1;
	}
	if (nb > 9)
		ft_putnbr_fd(nb / 10, fd);
	ch = (nb % 10) + 48;
	write(fd, &ch, 1);
}
